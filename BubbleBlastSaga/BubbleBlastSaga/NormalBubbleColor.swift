/// The different colors a normal bubble can have.
/// A normal bubble is one that does not exhibit any special properties
/// other than dropping when in a group of three or more bubbles
/// of the same color.
enum NormalBubbleColor: Int {
    case red = 0
    case orange = 1
    case blue = 2
    case green = 3
}
